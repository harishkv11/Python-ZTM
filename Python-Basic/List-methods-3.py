basket = [3,2,5,1,6,7]

#sort --> Sorts the list in ascending order
basket.sort()
print(basket)

#sorted --> Produces a new sorted list, doesn't modifies the list
basket = [3,2,5,1,6,7]
print(sorted(basket))
print(basket)

#copy --> creates a copy of list
new_list = basket.copy()
print(new_list)

#reverse --> Reverses the list
basket.reverse()
print(basket)