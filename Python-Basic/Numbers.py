#Arithmetic Operations
print(10+4) #Addition
print(10-4) #Subtraction
print(10*4) #Multiplication
print(10/4) #Division (Floating)

print(10//4) #Division (Integer)

#type() -> returns the type of input
print(type(3+4)) #int
print(type(1+1.9)) #int+float=float

#Exponentition (base**power)
print(2**3)

#Modulo -> Returns the remainder
print(10%4)
